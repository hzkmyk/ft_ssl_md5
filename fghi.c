#include "ft_ssl_md5.h"

int	f(int *iv)
{
	return ((iv[1] & iv[2]) | (~iv[1] & iv[3]));
}

int	g(int *iv)
{
	return ((iv[1] & iv[3]) | (iv[2] & ~ iv[3]));
}

int	h(int *iv)
{
	return (iv[1] ^ iv[2] ^ iv[3]);
}

int	i(int *iv)
{
	return (iv[2] ^ (iv[1] | ~iv[3]));
}